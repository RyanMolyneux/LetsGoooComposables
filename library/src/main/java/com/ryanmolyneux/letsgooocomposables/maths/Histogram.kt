/**
 * Types of data that can be visualised by the histogram along the X-axis.
 */
sealed class DataType {
    /**
     * Data class for the categorical type data, you are expected to provide precise names for each
     * any missing categories or misspelling entries will be ignored and not included in the final
     * visualization, undocumented categories that appear as entries in your data will be surrounded
     * by square brackets so as to make this apparent to you once they are graphed so you are aware
     * of this and if they are duplicates you can correct your data to fix it.
     */
    data class Categorical(
        val data: List<String>,
        val categories: List<String>,
        val barColours: List<Color> = listOf<Color>(
            Color.Red,
            Color.Green,
            Color.Blue
        )
    ) : DataType()

    /**
     * Data class for numerical type of data where the floating point value describes the space
     * that each range should take up including values within it that should be classified
     * as between said range of numbers.
     */
    data class Numerical(
        val data: List<Double>,
        val step: Float,
        val labelType: LabelType = LabelType.XStepLabel,
        val barColours: List<Color> = listOf<Color>(
            Color.Red,
            Color.Green,
            Color.Blue
        )
    ) : DataType() {
        sealed class LabelType {
            /**
             * Ranged labels are attached to the numerical histogram at each step
             * indicating the range of values covered by the current bar on the histograms
             * which is in the format lowest value-greatest value, where lowest value refers
             * to any value greater than or equal to it, while the greatest value only counts
             * those in the range which are lower than it's.
             */
            object XStepLabel : LabelType()

            /**
             * Independent x labeling from the histograms steps themselves, most commonly intended
             * for use in fine grain histograms known as frequency curves where the distribution
             * visualisation is much more important and practical than per histogram bar labels.
             */
            data class XLabel(val xLabelStep: Float) : LabelType();
        }
    }
}