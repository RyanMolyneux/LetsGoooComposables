/**
 * Histogram composable which currently as of today can graph both continous numerical data as well as
 * categorical data, visualising the data relative to its frequency of occurrence within a given
 * numerical or categorical range/bucket.
 */
@Composable
fun Histogram(dataType: DataType) {
    val textMeasurer = rememberTextMeasurer()

    Canvas(
        modifier = Modifier
            .fillMaxWidth()
            .fillMaxHeight()
    ) {
        val canvasHeight = this.size.height
        val canvasWidth = this.size.width
        val yTextWidth = textMeasurer.measure("100%").size.width.toFloat()
        val xTextHeight = textMeasurer.measure("HISTOGRAM").size.height.toFloat()
        fun DrawScope.drawLabel(
            text: String,
            xTextOffset: Float,
            textWidth: Float,
            yTextOffset: Float = (canvasHeight - xTextHeight)
        ) {
            drawText(
                textMeasurer = textMeasurer,
                text = AnnotatedString(text, SpanStyle(Color.Black), null),
                topLeft = Offset(xTextOffset, yTextOffset),
                size = Size(textWidth, xTextHeight)
            )
        }

        when (dataType) {
            is DataType.Categorical -> {
                val entryCountMap = dataType.run {
                    val map = mutableMapOf<String, Long>()

                    for (entry in data) {
                        map[entry] = (map.getOrDefault(entry, 0) + 1)
                    }

                    map
                }

                var index = 0
                var xOffset = yTextWidth
                for (category in entryCountMap.keys) {
                    val entryCount = entryCountMap.getOrDefault(category, 0).toFloat()
                    val categoryBarHeight =
                        ((canvasHeight - xTextHeight) * (entryCount / dataType.data.count()
                            .toFloat()))
                    drawRect(
                        dataType.barColours.getOrElse(index) { dataType.barColours.random() },
                        topLeft = Offset(xOffset, (canvasHeight - categoryBarHeight - xTextHeight)),
                        size = Size(
                            width = (canvasWidth / entryCountMap.keys.size),
                            height = categoryBarHeight
                        )
                    )
                    drawLine(
                        color = Color.Gray,
                        start = Offset(xOffset, (canvasHeight - xTextHeight)),
                        end = Offset(xOffset, canvasHeight)
                    )
                    if (dataType.categories.contains(category)) {
                        drawLabel(
                            text = category,
                            xTextOffset = xOffset,
                            textWidth = (canvasWidth / entryCountMap.keys.size.toFloat())
                        )
                    } else {
                        drawLabel(
                            text = "[$category]",
                            xTextOffset = xOffset,
                            textWidth = (canvasWidth / entryCountMap.keys.size.toFloat())
                        )
                    }
                    index++
                    xOffset += (canvasWidth / entryCountMap.keys.size.toFloat())
                }
            }

            is DataType.Numerical -> {
                val smallestDouble = dataType.data.findSmallest()!!
                val biggestDouble = dataType.data.findBiggest()!!
                val entryCountMap = dataType.run {
                    val map = mutableMapOf<String, Long>()

                    var currentStep = smallestDouble
                    while (currentStep <= biggestDouble) {
                        map["$currentStep - ${(currentStep + step)}"] =
                            dataType.data.countAllBetween(currentStep, (currentStep + step))
                        currentStep += step
                    }
                    map
                }

                var index = 0
                var xOffset = yTextWidth
                for (range in entryCountMap.keys) {
                    val entryCount = entryCountMap.getOrDefault(range, 0).toFloat()
                    val entryBarHeight =
                        ((canvasHeight - xTextHeight) * (entryCount / dataType.data.count()
                            .toFloat()))
                    drawRect(
                        dataType.barColours.getOrElse(index) { dataType.barColours.random() },
                        topLeft = Offset(xOffset, (canvasHeight - entryBarHeight - xTextHeight)),
                        size = Size(
                            width = ((canvasWidth - yTextWidth) / entryCountMap.keys.size),
                            height = entryBarHeight
                        )
                    )

                    if (dataType.labelType is DataType.Numerical.LabelType.XStepLabel) {
                        drawLine(
                            color = Color.Gray,
                            start = Offset(xOffset, (canvasHeight - xTextHeight)),
                            end = Offset(xOffset, canvasHeight)
                        )
                        drawLabel(
                            text = range,
                            xTextOffset = xOffset,
                            textWidth = ((canvasWidth - yTextWidth) / entryCountMap.keys.size.toFloat())
                        )
                    }
                    xOffset += ((canvasWidth - yTextWidth) / entryCountMap.keys.size.toFloat())
                    index++
                }
                if (dataType.labelType is DataType.Numerical.LabelType.XLabel) {
                    var currentStep = smallestDouble.toFloat()
                    xOffset = yTextWidth
                    while (currentStep < biggestDouble) {
                        drawLabel(
                            "$currentStep",
                            xOffset,
                            (canvasWidth * ((currentStep + 1f) / biggestDouble.toFloat()))
                        )
                        currentStep += dataType.labelType.xLabelStep
                        xOffset = (canvasWidth * ((currentStep) / biggestDouble.toFloat()))
                    }
                }
            }
        }
        drawLabel(text = "0%", xTextOffset = 0f, yTextWidth)
        for (i in 1..100) {
            if ((i % 10) == 0) {
                drawLabel(
                    text = "${i}%",
                    xTextOffset = 0f,
                    textWidth = yTextWidth,
                    yTextOffset = ((canvasHeight - xTextHeight) - ((canvasHeight - xTextHeight) * (i.toFloat() / 100f)))
                )
                drawLine(
                    color = Color.Gray,
                    start = Offset(
                        0f,
                        ((canvasHeight - xTextHeight) - ((canvasHeight - xTextHeight) * (i.toFloat() / 100f)))
                    ),
                    end = Offset(
                        yTextWidth,
                        ((canvasHeight - xTextHeight) - ((canvasHeight - xTextHeight) * (i.toFloat() / 100f)))
                    )
                )
            } else {
                drawLine(
                    color = Color.Gray,
                    start = Offset(
                        yTextWidth - (yTextWidth / 4f),
                        ((canvasHeight - xTextHeight) - ((canvasHeight - xTextHeight) * (i.toFloat() / 100f)))
                    ),
                    end = Offset(
                        yTextWidth,
                        ((canvasHeight - xTextHeight) - ((canvasHeight - xTextHeight) * (i.toFloat() / 100f)))
                    )
                )
            }
        }
        drawLine(
            color = Color.Gray,
            start = Offset(yTextWidth, 0f),
            end = Offset(yTextWidth, canvasHeight)
        )
        drawLine(
            color = Color.Gray,
            start = Offset(0f, (canvasHeight - xTextHeight)),
            end = Offset(canvasWidth, (canvasHeight - xTextHeight))
        )
    }
}


fun List<Double>.findSmallest(): Double? {
    var currentSmallest: Double? = null

    for (entry in this) {
        if (currentSmallest == null) {
            currentSmallest = entry
        } else if (currentSmallest > entry) {
            currentSmallest = entry
        }
    }

    return currentSmallest
}

fun List<Double>.findBiggest(): Double? {
    var currentBiggest: Double? = null

    for (entry in this) {
        if (currentBiggest == null) {
            currentBiggest = entry
        } else if (currentBiggest < entry) {
            currentBiggest = entry
        }
    }

    return currentBiggest
}

fun List<Double>.countAllBetween(greaterThanOrEqualTo: Double, lessThan: Double): Long {
    var count = 0L
    for (entry in this) {
        if (entry >= greaterThanOrEqualTo && entry < lessThan) {
            count++
        }
    }
    return count
}

